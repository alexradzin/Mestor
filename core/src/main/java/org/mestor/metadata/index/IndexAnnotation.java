package org.mestor.metadata.index;

import java.lang.annotation.ElementType;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlElementWrapper;
import javax.xml.bind.annotation.XmlType;

@XmlType(name = "index")
@XmlAccessorType(XmlAccessType.FIELD)
public class IndexAnnotation {
	@XmlElement(name = "class")
	private String className;
	@XmlElement
	private String name;
	@XmlElement(name = "column-names")
	private String columnNames;
	@XmlElement(name = "target")
	@XmlElementWrapper(name = "targets")
	private ElementType[] targets = new ElementType[] {ElementType.FIELD, ElementType.METHOD, ElementType.TYPE};
	
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getColumnNames() {
		return columnNames;
	}
	public void setColumnNames(String columnNames) {
		this.columnNames = columnNames;
	}
	public ElementType[] getTargets() {
		return targets;
	}
	public void setTargets(ElementType ... targets) {
		this.targets = targets;
	}
	
}
