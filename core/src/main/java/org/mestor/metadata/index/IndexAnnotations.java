package org.mestor.metadata.index;

import java.util.List;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlElements;
import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement(name = "index-annotations")
@XmlAccessorType(XmlAccessType.FIELD)
public class IndexAnnotations {
	@XmlElements({
		@XmlElement(name = "index", type = IndexAnnotation.class),
		@XmlElement(name = "index-container", type = IndexAnnotationContainer.class),
	})
	private List<?> indexDefs;

	public List<?> getIndexDefs() {
		return indexDefs;
	}

	public void setIndexDefs(List<?> indexDefs) {
		this.indexDefs = indexDefs;
	}
}
